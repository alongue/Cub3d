#include "libft.h"

int		ft_is_in_a_row(char *str, int c)
{
	int		i;

	i = -1;
	while (str[++i])
	{
		if (str[i] == '\0')
			return (0);
		else if (str[i] == c && str[i] == str[i + 1])
			return (1);
	}
	return (0);
}
